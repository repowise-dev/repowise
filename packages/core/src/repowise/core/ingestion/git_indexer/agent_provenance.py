"""Per-commit agent-provenance classification — deterministic, precision-first.

Labels a commit ``{agent, autonomy_tier, channel, confidence}`` from the
attribution channels available in **local git history** (identity fields,
message footers, ``Co-authored-by:`` trailers — no network, no API calls, no
LLM), in strip-resistance order:

  Tier 1 (near-autonomous — an agent service account authored the commit):
     * commit AUTHOR is a known agent bot account / service e-mail
       (``copilot-swe-agent[bot]``, ``devin-ai-integration[bot]``,
       ``cursoragent@cursor.com``, ...)
  Tier 2 (human-driven agent — the agent wrote the change):
     * commit-message footer (exact service phrases: "Generated with Claude
       Code", opencode, Codex) or the aider author-name suffix ``... (aider)``
     * a service identity as COMMITTER over a human author (the agent
       pushed/amended a human-driven change — deliberately NOT tier 1)
  Tier 3 (assisted — an agent contributed, a human authored):
     * ``Co-authored-by:`` trailer naming a known agent service identity
     * an ``Assisted-by:`` trailer naming a recognized agent (the Fedora /
       OpenInfra AI-attribution standard)

Three cross-cutting channels ride the same rule set:
  * a **git-ai authorship note** (``refs/notes/ai``, the git-ai standard) —
    an explicit, line-level attestation attached to the commit. Read out of
    band (see :mod:`git_commit_index`) and passed in pre-resolved as
    ``note_agent``; classified Tier 2 (``git_ai_note`` channel).
  * an **agent-trace record** (the vendor-neutral agent-trace standard,
    ``.agent-trace/traces.jsonl``) resolved to the commit by
    :class:`AgentTraceIndex` and passed in as ``trace_agent``; classified
    Tier 2 (``agent_trace`` channel).
  * a ``Generated-by:`` trailer (Apache's AI-attribution footer) naming a
    recognized agent — Tier 2 (``generated_by_trailer`` channel).

Precedence is tier order (1 > 2 > 3); first match wins. A commit with no
match is human (``None``). Every pattern is anchored to a *service identity*
(bot login, service e-mail, exact footer phrase) — never a bare name. A loose
name pattern is how a human contributor named "Devin" gets misclassified as
the Devin agent; a false "agent-authored" label on a human commit is worse
than a miss.

PR-level channels (bot PR author, agent branch prefixes, PR-body footers)
require the forge API and are intentionally out of scope here: squash merges
that strip commit trailers are therefore a known recall loss, not a precision
loss. ``confidence`` is carried per channel so future, weaker channels can be
filtered by consumers; every local channel is ``"high"``.

The default registry below is data, not code: repos can extend it from
``.repowise/config.yaml`` (see :func:`classifier_from_repo_config`) without
touching this module, e.g.::

    agent_provenance:
      service_emails:
        mybot@example.com: {agent: mybot, tier: 1}
      footer_patterns:
        - {pattern: "Generated by MyBot", agent: mybot}
      coauthor_patterns:
        - {pattern: "^co-authored-by: mybot <[^>]*@example\\.com>", agent: mybot}

Classification is pure in-memory regex over already-parsed commit records and
rides the existing commit-index walk — it adds no git subprocess and no I/O.
"""

